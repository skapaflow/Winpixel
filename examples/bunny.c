/* ── parse ────────────────────────────────────────────────────── */
static void parse_bunny(void) {
	const char *p = BUNNY;
	char *end;
	nv = (int)strtol(p, &end, 10); p = end + 1;
	nf = (int)strtol(p, &end, 10); p = end + 1;
	for (int i = 0; i < nv; i++) {
		vx[i] = strtof(p, &end); p = end + 1;
		vy[i] = strtof(p, &end); p = end + 1;
		vz[i] = strtof(p, &end); p = end + 1;
	}
	for (int i = 0; i < nf; i++) {
		strtol(p, &end, 10); p = end + 1;
		int a = (int)strtol(p, &end, 10) - 1; p = end + 1;
		int b = (int)strtol(p, &end, 10) - 1; p = end + 1;
		int c = (int)strtol(p, &end, 10) - 1; p = end + 1;
		fi[i][0] = a; fi[i][1] = b; fi[i][2] = c;
		/* per-face normal (cross product + normalization) */
		float ax = vx[b]-vx[a], ay = vy[b]-vy[a], az = vz[b]-vz[a];
		float bx = vx[c]-vx[a], by = vy[c]-vy[a], bz = vz[c]-vz[a];
		float nx = ay*bz-az*by, ny = az*bx-ax*bz, nz = ax*by-ay*bx;
		float len = sqrtf(nx*nx + ny*ny + nz*nz);
		if (len > 1e-6f) { nx/=len; ny/=len; nz/=len; }
		fn[i][0] = -nx; fn[i][1] = -ny; fn[i][2] = -nz;
	}
}

/* ── math 3×3 row-major — same as cube.c ─────────────────────── */
static void m3_identity(float m[9]) {
	memset(m, 0, 36); m[0] = m[4] = m[8] = 1.0f;
}
static void m3_mulv(const float m[9], const float v[3], float o[3]) {
	o[0] = m[0]*v[0] + m[1]*v[1] + m[2]*v[2];
	o[1] = m[3]*v[0] + m[4]*v[1] + m[5]*v[2];
	o[2] = m[6]*v[0] + m[7]*v[1] + m[8]*v[2];
}
static void m3_roty(float m[9], float a) {
	float c = cosf(a), s = sinf(a);
	m3_identity(m); m[0]=c; m[2]=s; m[6]=-s; m[8]=c;
}

/* ── projection — same as cube.c ──────────────────────────────── */
static void proj_v(const float v[3], float *sx, float *sy, float *sz) {
	float z = v[2] + CAM_Z;
	if (z < 0.01f) z = 0.01f;
	float w = FOCAL / z;
	*sx = SCR_W * 0.5f + v[0] * w;
	*sy = SCR_H * 0.5f - v[1] * w;
	*sz = z;
}

/* ── flat-dither rasterizer — copied from cube.c ─────────────── */
static void hline(int y, int x0, int x1,
				  uint16_t pat, Color32 dk, Color32 lt) {
	if (x0 > x1) { int t=x0; x0=x1; x1=t; }
	if (y<0||y>=SCR_H||x1<0||x0>=SCR_W) return;
	if (x0<0) x0=0;
	if (x1>=SCR_W) x1=SCR_W-1;
	int row = (y&3)<<2;
	for (int x=x0; x<=x1; x++)
		wpx_pixel(x, y, (pat >> (15-(x&3)-row)) & 1 ? lt : dk);
}

